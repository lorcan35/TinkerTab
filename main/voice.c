/* W4-A (cross-stack audit 2026-05-11): turn_id is a fresh per-turn
 * UUID stamped on every Tab5→Dragon turn-initiation frame (`start`,
 * `text`) AND included in every voice obs event for the same turn.
 * Pre-W4, correlating a Tab5 failure with the matching Dragon log
 * was timestamp+grep guessing.  W4-B (TinkerBox) will read + echo
 * the field so the full chain emits the same id.
 *
 * 13 chars (12 hex + null) — 48 bits of entropy, comfortably unique
 * across the lifetime of a session.  Generated by `gen_turn_id`. */
#define TURN_ID_LEN 13
static char s_current_turn_id[TURN_ID_LEN] = {0};

static void gen_turn_id(void) {
   uint32_t a = esp_random();
   uint32_t b = esp_random();
   snprintf(s_current_turn_id, sizeof s_current_turn_id, "%08lx%04lx", (unsigned long)a, (unsigned long)(b & 0xFFFFu));
   tab5_debug_obs_event("turn.start", s_current_turn_id);
}

const char *voice_current_turn_id(void) { return s_current_turn_id[0] ? s_current_turn_id : "-"; }

